package DTO;

import java.sql.Date;

public class DeThi {

	private int soCauHoi;
	private Date ngayMo;
	private int thoiGianLam;
	private int soCauDe;
	private int soCauKho;
	public int getSoCauHoi() {
		return soCauHoi;
	}
	public void setSoCauHoi(int soCauHoi) {
		this.soCauHoi = soCauHoi;
	}
	public Date getNgayMo() {
		return ngayMo;
	}
	public void setNgayMo(Date ngayMo) {
		this.ngayMo = ngayMo;
	}
	public int getThoiGianLam() {
		return thoiGianLam;
	}
	public void setThoiGianLam(int thoiGianLam) {
		this.thoiGianLam = thoiGianLam;
	}
	public int getSoCauDe() {
		return soCauDe;
	}
	public void setSoCauDe(int soCauDe) {
		this.soCauDe = soCauDe;
	}
	public int getSoCauKho() {
		return soCauKho;
	}
	public void setSoCauKho(int soCauKho) {
		this.soCauKho = soCauKho;
	}
	
	
}
